"use strict";
// Question No. 111
// Making a variable that define a Persons age
let personAge = 14;
// if-else-if chain to categorize a person's age group (child, teenager, adult)
if (personAge <= 13) {
    console.log(`You are a Child`);
}
else if (personAge > 13 && personAge <= 19) {
    console.log(`You are Teenager`);
}
else {
    console.log(`You are Adult`);
}
